#OPERADORES ARITMETICOS

print(3+4)
print(3-4)
print(3*4)
print(3/4)
print(35%9)
print(45//4)
print(4**4)

print("Hola "*3)

#OPERADORES COMPARATIVOS

print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)

#Comprobamos una ordenación alfabetica por ASCII
print("Hola" > "Python")
print("Hola" < "Python")
print(len("Hola") >= len("Zola")) # Cuenta caracteres
print("Python" == "Python")

#Operadores lógicos
print( 3 > 4 and "Hola" > "Python")
print( 3 > 4 or "Hola" < "Python")
print(not(3 > 4 and "Hola" > "Python"))